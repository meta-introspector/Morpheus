type ResponseData = {
    message: string
  }

export default async function handler(req: NextApiRequest, res: NextApiResponse<ResponseData>) {

    var token = req.headers.authorization;
    var sender = req.body.sender;
    var receiver = req.body.receiver;
    var amount = req.body.amount;

    const provider = new ethers.providers.InfuraProvider(10, process.env.INFURA_PROJECT_ID);

    const USDC_OPTIMISM_CONTRACT = "0x7f5c764cbc14f9669b88837ca1490cca17c31607";

    const USDC_SENDER_PRIVATE_KEY = process.env.USDC_SENDER_PRIVATE_KEY || "";

    const sender_wallet = new ethers.Wallet(USDC_SENDER_PRIVATE_KEY, provider);

    let address = sender_wallet.address;

    const value = ethers.utils.parseUnits(amount, 6)

    try {

        const contract = new ethers.Contract(USDC_OPTIMISM_CONTRACT, abi, sender_wallet);

        const transaction = await contract.transfer(receiver, value);

        console.log(transaction);

        console.log(`Sent ${ethers.utils.formatUnits(value, 6)} USDC to ${receiver}`);

        return res.status(200).json({ message: `Sent USDC to ${address}` })


    }

    catch (error) {
        console.log(error);
    }

}